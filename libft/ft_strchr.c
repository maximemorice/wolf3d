char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != 0 && s[i] != (char)c)
		i++;
	if (s[i] == 0 && c != 0)
		return (0);
	else
		return ((char *)&s[i]);
}
